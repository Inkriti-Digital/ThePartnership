            <h3>Carol Fulp<span>President and CEO of The Partnership, Inc.</span></h3>
            <p id="postDescript">
              Carol Fulp is President and CEO of The Partnership, Inc., New England’s premier organization dedicated to enhancing the competitiveness of the region by attracting, training, retaining and convening professionals of color.  During its 27 year history, The Partnership has collaborated with more than 250 corporate CEOs who have sponsored nearly 3,000 executives and professionals of color in the organization’s innovative leadership development programming. Fulp has significantly increased The Partnership’s clientele base resulting in a 25% increase in revenue. She expanded offerings by creating the C-Suite Program for executives of color at the highest levels of corporations, which launched this summer at the home of Governor Deval Patrick and First Lady Diane Patrick.
            </p>
              <p id="postMore">
                Prior to The Partnership, Fulp was Senior Vice President of Corporate Responsibility and Brand Management at John Hancock Financial. Here she led the company’s marketing and sponsorships departments, while directing the company’s $12 million philanthropic giving program. Under her leadership, the company created the largest corporate summer jobs program of its kind in the country and received the Points of Light Foundation’s Corporate Excellence Award.  Previously, Fulp was the Director of Community Programming and Human Resources for WCVB, the ABC-TV Boston affiliate. She also served as the Corporate Employee Relations Manager for the Gillette Company.<br/><br/>
                Given her leadership in business and public service, President Obama appointed Fulp as a Representative of the United States of America to the Sixty-fifth Session of the United Nations General Assembly. Governor Patrick appointed her as a trustee to the Massachusetts Convention Center Authority, Governor de Jongh appointed her as a trustee of the University of the Virgin Islands and Mayor Walsh appointed her as a trustee of the Boston Public Library.<br/><br/>
                Fulp also serves on the board of trustees for Eastern Bank and as well as the board of directors for American Student Assistance Corporation, where she currently chairs the search committee for president.  Her civic involvement includes the Harvard Kennedy School Women’s Leadership Board and trustee of the John F. Kennedy Presidential Library Foundation, where she co-chaired the Profile in Courage Awards Dinner.  In addition, she is on the board of trustees of Beth Israel Deaconess Medical Center and Citizen’s United for Research in Epilepsy (CURE). She is also a founding co-chair of the Massachusetts Conference for Women, the largest professional women’s conference in the country, attracting 10,000 attendees annually. Most recently Fulp joined the boards of Urban Improve and Boston 2024. <br/><br/>
                Fulp was a member of the Obama for America National Finance Committee.  She also served as a finance chair for Governor Patrick’s re-election campaign.  She was also appointed by Mayor Menino as co-chair of the Host Committee for the Democratic National Convention held in Boston.<br/><br/>
                She is the recipient of many honors including the Greater Boston Chamber of Commerce Pinnacle Award, Museum of Afro-American History’s Living Legend Award, Anti-Defamation League’s Women of Valor Award, Massachusetts Democratic Party’s Eleanor Roosevelt Award, Boston Bar Foundation’s Public Service Award, Manulife Corporation’s Global Citizen Award, United Nations of Greater Boston Global Leaders Award and South African Partners Corporate Citizen Award.  Boston Business Journal has listed Fulp as one of the “50 Most Influential Bostonians” and Boston Magazine listed her as one of the “50 Most Powerful Women in Boston.”<br/><br/>
                Fulp is a graduate of the University of the State of New York.  She is a recipient of an honorary doctorate of law from New England Law Boston and a honorary doctorate from Salem State University.  She is married to C. Bernard Fulp, Chairman of Go-Biz Solutions and Founder of Middlesex Bank &amp; Trust Company. They reside in Boston.

